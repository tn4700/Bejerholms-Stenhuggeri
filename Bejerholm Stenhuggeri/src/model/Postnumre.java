public class Postnumre {

    private int post_nr;
    private String byNavn;

    public Postnumre() {
        this.post_nr = post_nr;
        this.byNavn = byNavn;
    }

    public int getPost_nr() {
        return post_nr;
    }

    public void setPost_nr(int post_nr) {
        this.post_nr = post_nr;
    }

    public String getByNavn() {
        return byNavn;
    }

    public void setByNavn(String byNavn) {
        this.byNavn = byNavn;
    }
}
